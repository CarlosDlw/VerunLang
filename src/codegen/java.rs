use std::collections::HashSet;

use crate::ast::nodes::*;
use crate::ast::types::Type;

use super::target::CodeTarget;

pub struct JavaTarget;

impl CodeTarget for JavaTarget {
    fn name(&self) -> &str {
        "java"
    }

    fn file_extension(&self) -> &str {
        "java"
    }

    fn generate(&self, program: &Program) -> String {
        let mut out = String::new();
        out.push_str("// Generated by Verun.\n\n");

        let top_consts: Vec<&ConstDef> = program
            .items
            .iter()
            .filter_map(|i| match &i.node {
                Item::ConstDef(c) => Some(c),
                _ => None,
            })
            .collect();
        let top_const_names: HashSet<String> = top_consts.iter().map(|c| c.name.node.clone()).collect();
        if !top_consts.is_empty() {
            out.push_str("final class VerunConsts {\n");
            for c in top_consts {
                out.push_str(&format!(
                    "    static final {} {} = {};\n",
                    self.type_to_java(&c.ty.node),
                    c.name.node,
                    self.expr_to_java(
                        &c.value.node,
                        &HashSet::new(),
                        &HashSet::new(),
                        &top_const_names,
                        false,
                    )
                ));
            }
            out.push_str("}\n\n");
        }

        for item in &program.items {
            match &item.node {
                Item::EnumDef(e) => out.push_str(&self.gen_enum(e)),
                Item::TypeDef(t) => out.push_str(&self.gen_type_def(t)),
                Item::State(s) => out.push_str(&self.gen_state(s, &top_const_names)),
                _ => {}
            }
        }

        out
    }
}

impl JavaTarget {
    fn gen_enum(&self, e: &crate::ast::types::EnumDef) -> String {
        let mut out = format!("enum {} {{\n", e.name.node);
        for (i, variant) in e.variants.iter().enumerate() {
            if i + 1 == e.variants.len() {
                out.push_str(&format!("    {}\n", variant.node));
            } else {
                out.push_str(&format!("    {},\n", variant.node));
            }
        }
        out.push_str("}\n\n");
        out
    }

    fn gen_type_def(&self, t: &crate::ast::types::TypeDef) -> String {
        let mut out = format!("class {} {{\n", t.name.node);
        for field in &t.fields {
            out.push_str(&format!("    public {} {};\n", self.type_to_java(&field.ty.node), field.name.node));
        }
        out.push_str("}\n\n");
        out
    }

    fn gen_state(&self, state: &StateDef, top_consts: &HashSet<String>) -> String {
        let mut out = format!("public class {} {{\n", state.name.node);

        for c in &state.constants {
            out.push_str(&format!(
                "    public static final {} {} = {};\n",
                self.type_to_java(&c.ty.node),
                c.name.node,
                self.expr_to_java(&c.value.node, &HashSet::new(), &HashSet::new(), top_consts, false)
            ));
        }
        if !state.constants.is_empty() {
            out.push('\n');
        }

        for field in &state.fields {
            out.push_str(&format!("    public {} {};\n", self.type_to_java(&field.ty.node), field.name.node));
        }
        out.push('\n');

        out.push_str(&format!("    public {}() {{\n", state.name.node));
        if let Some(init) = &state.init {
            for assign in &init.assignments {
                out.push_str(&format!(
                    "        this.{} = {};\n",
                    assign.target.node,
                    self.expr_to_java(&assign.value.node, &HashSet::new(), &HashSet::new(), top_consts, false)
                ));
            }
        }
        out.push_str("    }\n\n");

        for transition in &state.transitions {
            out.push_str(&self.gen_transition(state, transition, top_consts));
        }

        out.push_str("}\n\n");
        out
    }

    fn gen_transition(&self, state: &StateDef, t: &Transition, top_consts: &HashSet<String>) -> String {
        let params: Vec<String> = t
            .params
            .iter()
            .map(|p| format!("{} {}", self.type_to_java(&p.ty.node), p.name.node))
            .collect();
        let params_set: HashSet<String> = t.params.iter().map(|p| p.name.node.clone()).collect();
        let fields_set: HashSet<String> = state.fields.iter().map(|f| f.name.node.clone()).collect();

        let mut out = format!("    public void {}({}) {{\n", t.name.node, params.join(", "));

        for pre in &t.preconditions {
            out.push_str(&format!(
                "        if (!({})) throw new RuntimeException(\"precondition failed\");\n",
                self.expr_to_java(&pre.node, &params_set, &fields_set, top_consts, false)
            ));
        }

        if !t.postconditions.is_empty() {
            for field in &state.fields {
                out.push_str(&format!("        var _old_{} = this.{};\n", field.name.node, field.name.node));
            }
        }

        for stmt in &t.body {
            let stmt_code = self.stmt_to_java(&stmt.node, &params_set, &fields_set, top_consts);
            if !stmt_code.is_empty() {
                if self.is_block_statement(&stmt.node) {
                    out.push_str(&format!("        {}\n", stmt_code));
                } else {
                    out.push_str(&format!("        {};\n", stmt_code));
                }
            }
        }

        for inv in &state.invariants {
            let inv_name = inv.name.as_ref().map(|n| n.node.as_str()).unwrap_or("invariant");
            out.push_str(&format!(
                "        if (!({})) throw new RuntimeException(\"invariant '{}' violated\");\n",
                self.expr_to_java(&inv.condition.node, &params_set, &fields_set, top_consts, false),
                inv_name
            ));
        }

        for post in &t.postconditions {
            out.push_str(&format!(
                "        if (!({})) throw new RuntimeException(\"postcondition violated\");\n",
                self.expr_to_java(&post.node, &params_set, &fields_set, top_consts, false)
            ));
        }

        out.push_str("    }\n\n");
        out
    }

    fn type_to_java(&self, ty: &Type) -> String {
        match ty {
            Type::Int => "long".to_string(),
            Type::Real => "double".to_string(),
            Type::Bool => "boolean".to_string(),
            Type::String => "String".to_string(),
            Type::Named(name) | Type::Enum(name) => name.clone(),
            Type::Array { element, size: _ } => format!("{}[]", self.type_to_java(element)),
            Type::Map { key, value } => format!("java.util.Map<{}, {}>", self.boxed_java_type(key), self.boxed_java_type(value)),
        }
    }

    fn boxed_java_type(&self, ty: &Type) -> String {
        match ty {
            Type::Int => "Long".to_string(),
            Type::Real => "Double".to_string(),
            Type::Bool => "Boolean".to_string(),
            Type::String => "String".to_string(),
            Type::Named(name) | Type::Enum(name) => name.clone(),
            Type::Array { element, .. } => format!("{}[]", self.boxed_java_type(element)),
            Type::Map { key, value } => format!("java.util.Map<{}, {}>", self.boxed_java_type(key), self.boxed_java_type(value)),
        }
    }

    fn expr_to_java(
        &self,
        expr: &Expr,
        params: &HashSet<String>,
        fields: &HashSet<String>,
        top_consts: &HashSet<String>,
        in_old: bool,
    ) -> String {
        match expr {
            Expr::IntLit(v) => v.to_string(),
            Expr::RealLit(v) => v.to_string(),
            Expr::BoolLit(v) => v.to_string(),
            Expr::StringLit(v) => format!("\"{}\"", v),
            Expr::Ident(name) => {
                if params.contains(name) {
                    name.clone()
                } else if in_old {
                    format!("_old_{}", name)
                } else if fields.contains(name) {
                    format!("this.{}", name)
                } else if top_consts.contains(name) {
                    format!("VerunConsts.{}", name)
                } else {
                    name.clone()
                }
            }
            Expr::Old(inner) => self.expr_to_java(&inner.node, params, fields, top_consts, true),
            Expr::EnumVariant { enum_name, variant } => format!("{}.{}", enum_name, variant),
            Expr::UnaryOp { op, operand } => {
                let inner = self.expr_to_java(&operand.node, params, fields, top_consts, in_old);
                match op {
                    UnaryOp::Neg => format!("(-{})", inner),
                    UnaryOp::Not => format!("(!{})", inner),
                }
            }
            Expr::BinaryOp { left, op, right } => {
                let l = self.expr_to_java(&left.node, params, fields, top_consts, in_old);
                let r = self.expr_to_java(&right.node, params, fields, top_consts, in_old);
                let op_str = match op {
                    BinaryOp::Add => "+",
                    BinaryOp::Sub => "-",
                    BinaryOp::Mul => "*",
                    BinaryOp::Div => "/",
                    BinaryOp::Mod => "%",
                    BinaryOp::Eq => "==",
                    BinaryOp::Neq => "!=",
                    BinaryOp::Lt => "<",
                    BinaryOp::Gt => ">",
                    BinaryOp::Lte => "<=",
                    BinaryOp::Gte => ">=",
                    BinaryOp::And => "&&",
                    BinaryOp::Or => "||",
                    BinaryOp::Implies => return format!("((!{}) || {})", l, r),
                };
                format!("({} {} {})", l, op_str, r)
            }
            Expr::FieldAccess { object, field } => {
                format!(
                    "{}.{}",
                    self.expr_to_java(&object.node, params, fields, top_consts, in_old),
                    field.node
                )
            }
            Expr::IndexAccess { object, index } => {
                format!(
                    "{}[{}]",
                    self.expr_to_java(&object.node, params, fields, top_consts, in_old),
                    self.expr_to_java(&index.node, params, fields, top_consts, in_old)
                )
            }
            Expr::MapAccess { map, key } => {
                format!(
                    "{}.get({})",
                    self.expr_to_java(&map.node, params, fields, top_consts, in_old),
                    self.expr_to_java(&key.node, params, fields, top_consts, in_old)
                )
            }
            Expr::FnCall { name, args } => {
                let arg_strs: Vec<String> = args
                    .iter()
                    .map(|a| self.expr_to_java(&a.node, params, fields, top_consts, in_old))
                    .collect();
                match name.node.as_str() {
                    "abs" => format!("Math.abs({})", arg_strs[0]),
                    "min" => format!("Math.min({}, {})", arg_strs[0], arg_strs[1]),
                    "max" => format!("Math.max({}, {})", arg_strs[0], arg_strs[1]),
                    _ => format!("{}({})", name.node, arg_strs.join(", ")),
                }
            }
            _ => "/* unsupported */".to_string(),
        }
    }

    fn stmt_to_java(
        &self,
        stmt: &Statement,
        params: &HashSet<String>,
        fields: &HashSet<String>,
        top_consts: &HashSet<String>,
    ) -> String {
        match stmt {
            Statement::Assign(assign) => {
                if let Expr::Ident(name) = &assign.value.node {
                    if *name == assign.target.node {
                        return String::new();
                    }
                }
                format!(
                    "this.{} = {}",
                    assign.target.node,
                    self.expr_to_java(&assign.value.node, params, fields, top_consts, false)
                )
            }
            Statement::CompoundAssign { target, op, value } => {
                let op_str = match op {
                    CompoundOp::Add => "+=",
                    CompoundOp::Sub => "-=",
                    CompoundOp::Mul => "*=",
                    CompoundOp::Div => "/=",
                };
                format!(
                    "this.{} {} {}",
                    target.node,
                    op_str,
                    self.expr_to_java(&value.node, params, fields, top_consts, false)
                )
            }
            Statement::IndexedAssign { target, index, value } => format!(
                "this.{}[{}] = {}",
                target.node,
                self.expr_to_java(&index.node, params, fields, top_consts, false),
                self.expr_to_java(&value.node, params, fields, top_consts, false)
            ),
            Statement::IndexedCompoundAssign { target, index, op, value } => {
                let op_str = match op {
                    CompoundOp::Add => "+=",
                    CompoundOp::Sub => "-=",
                    CompoundOp::Mul => "*=",
                    CompoundOp::Div => "/=",
                };
                format!(
                    "this.{}[{}] {} {}",
                    target.node,
                    self.expr_to_java(&index.node, params, fields, top_consts, false),
                    op_str,
                    self.expr_to_java(&value.node, params, fields, top_consts, false)
                )
            }
            Statement::Assert { condition } => format!(
                "if (!({})) throw new RuntimeException(\"assertion failed\")",
                self.expr_to_java(&condition.node, params, fields, top_consts, false)
            ),
            Statement::If { condition, then_body, else_body } => {
                let mut out = format!(
                    "if ({}) {{\n",
                    self.expr_to_java(&condition.node, params, fields, top_consts, false)
                );
                for s in then_body {
                    let stmt_code = self.stmt_to_java(&s.node, params, fields, top_consts);
                    if !stmt_code.is_empty() {
                        if self.is_block_statement(&s.node) {
                            out.push_str(&format!("            {}\n", stmt_code));
                        } else {
                            out.push_str(&format!("            {};\n", stmt_code));
                        }
                    }
                }
                out.push_str("        }");
                if let Some(else_stmts) = else_body {
                    out.push_str(" else {\n");
                    for s in else_stmts {
                        let stmt_code = self.stmt_to_java(&s.node, params, fields, top_consts);
                        if !stmt_code.is_empty() {
                            if self.is_block_statement(&s.node) {
                                out.push_str(&format!("            {}\n", stmt_code));
                            } else {
                                out.push_str(&format!("            {};\n", stmt_code));
                            }
                        }
                    }
                    out.push_str("        }");
                }
                out
            }
            Statement::Let { name, ty, value } => {
                let declared = ty
                    .as_ref()
                    .map(|t| self.type_to_java(&t.node))
                    .unwrap_or_else(|| "var".to_string());
                format!(
                    "{} {} = {}",
                    declared,
                    name.node,
                    self.expr_to_java(&value.node, params, fields, top_consts, false)
                )
            }
            Statement::Match { expr, arms } => {
                let subject = self.expr_to_java(&expr.node, params, fields, top_consts, false);
                let mut out = format!("switch ({}) {{\n", subject);
                for arm in arms {
                    match &arm.pattern.node {
                        MatchPattern::Wildcard => out.push_str("            default:\n"),
                        _ => out.push_str(&format!("            case {}:\n", self.match_pattern_java(&arm.pattern.node))),
                    }
                    for s in &arm.body {
                        let stmt_code = self.stmt_to_java(&s.node, params, fields, top_consts);
                        if !stmt_code.is_empty() {
                            if self.is_block_statement(&s.node) {
                                out.push_str(&format!("                {}\n", stmt_code));
                            } else {
                                out.push_str(&format!("                {};\n", stmt_code));
                            }
                        }
                    }
                    out.push_str("                break;\n");
                }
                out.push_str("        }");
                out
            }
        }
    }

    fn is_block_statement(&self, stmt: &Statement) -> bool {
        matches!(stmt, Statement::If { .. } | Statement::Match { .. })
    }

    fn match_pattern_java(&self, p: &MatchPattern) -> String {
        match p {
            MatchPattern::EnumVariant { enum_name: _, variant } => variant.clone(),
            MatchPattern::IntLit(v) => v.to_string(),
            MatchPattern::BoolLit(v) => v.to_string(),
            MatchPattern::StringLit(v) => format!("\"{}\"", v),
            MatchPattern::Wildcard => "default".to_string(),
        }
    }
}
